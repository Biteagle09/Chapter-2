void DeleteX(LinkList &L, int x) {
	LNode* p;
	if (L ==  NULL) {
		return;
	}
	if (L->data == x) {
		p = L;
		L = L->next;
		free(p);
		DeleteX(L, x);
	}else{
		DeleteX(L->next,x);
	}
}